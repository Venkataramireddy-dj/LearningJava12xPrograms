package StringsAssesement;

public class Basic {
    public static void main(String[] args) {
        String word = "Hello world";
        System.out.println(word + "123");
        System.out.println(" Concatination : " + word + "123");
        System.out.println(" Length : " + word.length());
        System.out.println(" Substring : " + word.substring(6));
        System.out.println(" Character extraction : " + word.charAt(0));

    }
}
