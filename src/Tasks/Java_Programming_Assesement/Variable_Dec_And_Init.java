package Tasks.Java_Programming_Assesement;

public class Variable_Dec_And_Init {
    public static void main(String[] args) {
        int studentCount = 25;
        double avgScore = 87.5;
        boolean isPassing = true;
        char grade = 'A';
        byte fileSize = 120;
        short temperature = -17;
        long population = 7896543210L;
        float piValue = 3.14159f;
        System.out.println("Number of students: " + studentCount);
        System.out.println("Average test score: " + avgScore);
        System.out.println("Is student passing? " + isPassing);
        System.out.println("Letter grade: " + grade);
        System.out.println("File size in bytes: " + fileSize);
        System.out.println("Temperature in Celsius: " + temperature);
        System.out.println("World population: " + population);
        System.out.println("Value of pi: " + piValue);

    }
}
